                    <h1>Política de privacidad</h1>
                    <p>La secretaría de cultura del municipio de El Santuario pretende ofrecer la mejor experiencia de navegación a cada uno de sus usuarios.
                        Por ello nos esforzamos encarecidamente en proteger su privacidad presentando este documento
                        donde detallamos nuestros procedimientos y políticas respecto a la información y datos
                        personales. Datos que son recopilados a través del sitio web y/o plataformas virtuales de
                        la secretaria de cultura.</p>
                    <p>Los usuarios que acceden a nuestros servicios a través de la plataforma aceptan estar sujetos a
                        las disposiciones de nuestra Política de Privacidad. Acepta que se recopile su información
                        personal a través de nuestras encuestas, formularios electrónicos, entre otros, bien sea
                        otorgados y/o requeridos de forma virtual por la plataforma.</p>
                    <p>La empresa garantiza que la información personal brindada cuenta con la seguridad necesaria y no
                        será entregada a terceros. Solo se requerirán o validarán datos en casos de incumplimiento de
                        alguna orden judicial o requerimientos legales.</p>
                    <p>La suscripción a boletines de correos electrónicos es voluntaria y se le dará la opción de
                        seleccionarla al momento de crear su cuenta y en cualquier momento usted podrá darse de baja.
                    </p>
                    <p>Es necesario acotar que la presente política forma parte de nuestros Términos y Condiciones que
                        podrán encontrar en Terminos y condiciones de uso. Asimismo, la entidad se reserva los derechos
                        de modificar la Política de Privacidad sin previo aviso.</p>
                    <h2>¿Qué información recopilamos?</h2>
                    La secretaria de cultura recopila el siguiente tipo de información:
                    <ol>
                        <li>
                            Los datos que nos envías al momento de registrarte en nuestra plataforma o al tratar de
                            comunicarte con nosotros. Por ejemplo: el correo electrónico, fechas de nacimiento, nombres
                            completos, entre otros.
                        </li>
                        <li>
                            Información sobre las páginas de la entidad a la que accedes, en este caso serían los datos
                            sobre tu dispositivo: sistema operativo o tipo de navegador.
                        </li>
                        <li>
                            Recolectaremos tus datos de usuario en cuanto al contenido compartido dentro de nuestra
                            plataforma, las páginas de la secretaria de cultura a las que se accede y los datos de la multimedia
                            que te interesa.
                        </li>
                    </ol>
                    Es importante destacar que nuestra tecnología nos permite identificar datos como la ubicación
                    geográfica del usuario (país y ciudad) e historial de navegación por nuestro sitio web y aplicación.
                    Esto en caso de reportar un problema y tengamos la necesidad de ver cuáles páginas ha usado el
                    usuario e identificar mejor su caso.                    
                    
                    <p>El usuario tiene la opción de añadir información a su perfil, nos referimos a incluir una foto de
                        perfil, una breve descripción, país y ciudad donde vive o proviene y fecha de nacimiento.</p>
                    <h2>Divulgación de tu información</h2>
                    La secretaria de cultura utilizará la información recopilada para:
                    <ol>
                        <li>Proporcionar información que el usuario facilite.</li>
                        <li>Notificar al usuario sobre cambios en la plataforma.</li>
                        <li>Permitir al usuario participar en actividades interactivas de la plataforma.</li>
                        <li>Brindar soporte al usuario.</li>
                        <li>Validar la veracidad de la información brindada.</li>
                        <li>Atender las consultas y solicitudes del usuario.</li>
                        <li>Gestionar las preferencias del usuario referente a su cuenta.</li>
                        <li>Realizar estudios estadísticos para potenciar el funcionamiento de los servicios prestados
                            por la secretaria de cultura.</li>
                        <li>Garantizar el cumplimiento de los protocolos de seguridad y otros aspectos relacionados a
                            los servicios brindados por la secretaria de cultura.</li>
                        <li>Facilitar el funcionamiento técnico de los servicios, lo cual incluye solución de problemas,
                            seguridad de los servicios, prevención de fraude y uso indebido del contenido de la entidad.
                        </li>
