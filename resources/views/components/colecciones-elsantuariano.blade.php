<div class=" text-left text-sm mt-4 text-[#08416b] mr-24 ml-24" style="font-family:Raleway-Regular">
    <p>
        La revista El Santuariano y su colección “El Santuariano Histórico” es una publicación de gran valor cultural y
        periodístico en El Santuario. Fundado el 7 de julio de 1920 por Eusebio María Gómez Ramírez y Filemón de J Gómez
        Salazar (padre e hijo) este medio ha sido un pilar de la identidad local y la democracia de la región.
        Inicialmente asociado al centro cultural Liceo León XIII que en 1932 dio origen a la Sociedad de Mejoras
        Públicas.
    </p>
    <br>
    <p>
        A lo largo de los años, El Santuariano ha sido reconocido por su compromiso con la libertad de expresión y su
        contribución a la historia local. Con motivo de su centésimo aniversario, la Gobernación de Antioquia le otorgó
        un Decreto Especial de Reconocimiento por su labor en el periodismo independiente, cívico y cultural, destacando
        su importancia en el fortalecimiento del amor y sentido de pertenencia a la identidad municipal.
    </p>
</div>
