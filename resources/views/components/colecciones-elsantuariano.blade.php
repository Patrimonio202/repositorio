<div class=" text-left text-sm mt-4 text-[#08416b] mr-24 ml-24" style="font-family:Raleway-Regular">
    <p>
        Hasta la fecha, se han publicado más de 527 ediciones de El Santuariano, documentando el desarrollo y progreso
        de El Santuario. Además, la revista ha recibido otros reconocimientos como la Orden de la Democracia Simón
        Bolívar en el Grado Cruz Comendador, resaltando su influencia en la formación de profesionales y su servicio
        público en la comunicación.
    </p>
    <br>
    <p>
        El reconocido periodista Víctor León Zuluaga Salazar, figura central en el periodismo de El Santuario es su
        director desde 1975. Su carrera abarca diversos roles en medios como El Colombiano, así como una importante
        carrera académica que lo llevó a desempeñarse como decano de comunicaciones en la Universidad de Medellín y como
        docente en la Universidad de Antioquia durante 20 años. Su liderazgo ha sido crucial para mantener a El
        Santuariano como un medio relevante y respetado, no solo en El Santuario sino en muchas ciudades de Colombia.
    </p>
    <p>
        Esta colección, ahora disponible en línea, también incluye algunas ediciones de “Santuario”, periódico
        manuscrito que circuló desde el 26 de abril de 1908 hasta 1920 cuando se transformó en El Santuariano.
    </p>
